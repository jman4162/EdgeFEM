def main():
    parser = argparse.ArgumentParser(description='Mesh Convergence Study')
    parser.add_argument('--output-dir', default='convergence_data',
                        help='Output directory for results')
    parser.add_argument('--build-dir', default='build',
                        help='EdgeFEM build directory')
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    output_dir.mkdir(exist_ok=True)

    # Mesh sizes: lambda/5, lambda/10, lambda/15, lambda/20, lambda/30
    mesh_sizes = [
        (5, wavelength / 5),
        (10, wavelength / 10),
        (15, wavelength / 15),
        (20, wavelength / 20),
        (30, wavelength / 30),
    ]

    print("=== Mesh Convergence Study ===")
    print(f"Wavelength: {wavelength * 1000:.2f} mm")
    print(f"Output directory: {output_dir}")
    print()

    results = []

    for lambda_per_h, h in mesh_sizes:
        print(f"Generating mesh with h = λ/{lambda_per_h} = {h*1000:.2f} mm...")
        mesh_name = f"wr90_h{lambda_per_h}.msh"
        mesh_path = str(output_dir / mesh_name)

        if generate_waveguide_mesh(h, mesh_path):
            print(f"  Mesh saved to {mesh_path}")

            # Count mesh statistics
            with open(mesh_path) as f:
                content = f.read()
                # Simple parsing to count nodes and elements
                if '$Nodes' in content:
                    nodes_section = content.split('$Nodes')[1].split('$EndNodes')[0]
                    n_nodes = int(nodes_section.strip().split('\n')[0])
                else:
                    n_nodes = 0

                if '$Elements' in content:
                    elements_section = content.split('$Elements')[1].split('$EndElements')[0]
                    n_elements = int(elements_section.strip().split('\n')[0])
                else:
                    n_elements = 0

            results.append({
                'lambda_per_h': lambda_per_h,
                'h_mm': h * 1000,
                'n_nodes': n_nodes,
                'n_elements': n_elements,
                'mesh_path': mesh_path,
            })
            print(f"  Nodes: {n_nodes}, Elements: {n_elements}")
        else:
            print(f"  Failed to generate mesh")
        print()

    # Save mesh statistics
    stats_path = output_dir / 'mesh_statistics.json'
    with open(stats_path, 'w') as f:
        json.dump(results, f, indent=2)
    print(f"Mesh statistics saved to {stats_path}")

    # Print summary table
    print("\n=== Mesh Summary ===")
    print(f"{'λ/h':>6} {'h (mm)':>10} {'Nodes':>10} {'Elements':>10}")
    print("-" * 40)
    for r in results:
        print(f"{r['lambda_per_h']:>6} {r['h_mm']:>10.2f} {r['n_nodes']:>10} {r['n_elements']:>10}")

    print("\nTo run simulations, use the generated meshes with EdgeFEM.")
    print("Example:")
    print(f"  ./build/tests/test_mesh_convergence {output_dir}/wr90_h10.msh")

if __name__ == '__main__':
    main()
