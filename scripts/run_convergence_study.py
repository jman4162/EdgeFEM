wavelength = c0 / freq

def generate_waveguide_mesh(mesh_size: float, output_path: str) -> bool:
    """Generate WR-90 waveguide mesh using Gmsh."""
    geo_content = f"""// WR-90 Waveguide - Convergence Study
// Generated by run_convergence_study.py

lc = {mesh_size};

// Waveguide dimensions in meters
a = {a};
b = {b};
L = {L};

// Cross-section at z=0
Point(1) = {{0, 0, 0, lc}};
Point(2) = {{a, 0, 0, lc}};
Point(3) = {{a, b, 0, lc}};
Point(4) = {{0, b, 0, lc}};

Line(1) = {{1, 2}};
Line(2) = {{2, 3}};
Line(3) = {{3, 4}};
Line(4) = {{4, 1}};

Curve Loop(1) = {{1, 2, 3, 4}};
Plane Surface(1) = {{1}};

// Extrude to create waveguide
Extrude {{0, 0, L}} {{ Surface{{1}}; }}

// Physical groups
Physical Surface("PEC", 1) = {{13, 17, 21, 25}};
Physical Surface("Port1", 2) = {{1}};
Physical Surface("Port2", 3) = {{26}};
Physical Volume("Air", 100) = {{1}};

Mesh.Algorithm3D = 4;
Mesh.Optimize = 1;
"""
    geo_path = output_path.replace('.msh', '.geo')
    with open(geo_path, 'w') as f:
        f.write(geo_content)

    result = subprocess.run(
        ['gmsh', '-3', '-format', 'msh2', '-o', output_path, geo_path],
        capture_output=True, text=True
    )

    if result.returncode != 0:
        print(f"Gmsh error: {result.stderr}")
        return False
    return True

